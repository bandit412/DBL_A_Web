CREATE TABLE Store
(
	StoreID	  		INT 						NOT NULL	GENERATED ALWAYS AS IDENTITY,
	StoreName		VARCHAR(30)					NOT NULL,
	StoreLocation	VARCHAR(30)					NOT NULL,
	CONSTRAINT PK_Store_StoreID PRIMARY KEY (StoreID)
);

CREATE TABLE PaymentMethod
(
	PaymentMethodID	INT 						NOT NULL	GENERATED ALWAYS AS IDENTITY,
	MethodName		VARCHAR(20)					NOT NULL
		CONSTRAINT DF_PaymentMethod_MethodName DEFAULT('Debit'),
	CONSTRAINT PK_PaymentMethod_PaymetMethodID PRIMARY KEY(PaymentMethodID)
);

CREATE TABLE Transactions
(
	TransactionsID	INT 						NOT NULL 	GENERATED ALWAYS AS IDENTITY,
	TransactionDate	DATE						NOT NULL
		CONSTRAINT DF_Transactions_TransactionDate DEFAULT CURRENT_DATE,
	StoreID			INT							NOT NULL
		CONSTRAINT FK_Transactions_StoreID 		REFERENCES Store(StoreID),
	Items			VARCHAR(250)				NOT NULL,	
	Amount			NUMERIC(8,2)				NOT NULL,
	GST				NUMERIC(6,2)				NOT NULL,
	PaymentID		INT							NOT NULL
		CONSTRAINT FK_Transactions_PaymentID 	REFERENCES PaymentMethod(PaymentMethodId),
	CONSTRAINT PK_TransactionsTransactionsID PRIMARY KEY(TransactionsID)
);

CREATE TABLE Markets
(
	MarketsID		INT 						NOT NULL 	GENERATED ALWAYS AS IDENTITY,
	Market			VARCHAR(60)					NOT NULL,
	CONSTRAINT PK_Markets_MarketID PRIMARY KEY(MarketsID)
);

CREATE TABLE MarketSales
(
	MarketSalesID	INT 						NOT NULL	GENERATED ALWAYS AS IDENTITY,
	MarketID		INT							NOT NULL
		CONSTRAINT FK_MarketSales_MarketID 		REFERENCES Markets(MarketsID),
	MarketDate		DATE						NOT NULL,
	Amount			NUMERIC(6,2)				NOT NULL,
	Description		VARCHAR(60)					NOT NULL,
	MethodID		INT							NOT NULL
		CONSTRAINT FK_MarketSales_PaymentID 	REFERENCES PaymentMethod(PaymentMethodID),
	CONSTRAINT PK_MarketSales_MarketSalesID PRIMARY KEY(MarketSalesID)
);

CREATE TABLE DB_User_Role
(
	RoleID			INT							NOT NULL	GENERATED ALWAYS AS IDENTITY,
	Rolename		VARCHAR(10)					NOT NULL,
	CONSTRAINT PK_DB_User_Role_RoleID PRIMARY KEY(RoleID)
);

CREATE TABLE DB_User
(
	UserID			INT							NOT NULL	GENERATED ALWAYS AS IDENTITY,
	Username		VARCHAR(20)					NOT NULL	UNIQUE,
	UserEmail		VARCHAR(60)					NOT NULL	UNIQUE,
	UserPassword	VARCHAR(60)					NOT NULL,
	RoleID			INT							NOT NULL
		CONSTRAINT FK_DB_User_RoleID			REFERENCES DB_User_Role(RoleID),
	CONSTRAINT PK_DB_User_UserID PRIMARY KEY(UserID)
);

